CREATE TABLE product (
  id VARCHAR2(100)
  name VARCHAR2(100)
  coffea VARCHAR2(100)
  varietal VARCHAR2(100)
  origin VARCHAR2(100)
  roasting VARCHAR2(100) /* include the different options "natural", "high-roast", "mixture" */
  dcafprocess VARCHAR2(100)
  format VARCHAR2(100)
  packaging VARCHAR2(100)
)

